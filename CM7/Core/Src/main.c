/* USER CODE BEGIN 0 */
int __io_getchar(void) {
    uint8_t ch = 0;
    while (HAL_UART_Receive(&hcom_uart[COM1], &ch, 1, HAL_MAX_DELAY) != HAL_OK) {}
    // キーボードの入力を画面にそのままエコーバックする
    HAL_UART_Transmit(&hcom_uart[COM1], &ch, 1, 0xFFFF);
    return ch;
}
// モーターを動かす関数（引数は -100.0% 〜 100.0% のパワー）
void set_throttle_motor(float power_percent) {
    // 1. 回転方向（DIR）の決定
    if (power_percent > 0.0f) {
        // プラスなら開く方向 (環境に合わせてSET/RESETは逆にしてください)
        HAL_GPIO_WritePin(MOTOR_DIR_GPIO_Port, MOTOR_DIR_Pin, GPIO_PIN_SET);
    } else {
        // マイナスなら閉じる方向（バネに逆らって強制的に閉じる）
        HAL_GPIO_WritePin(MOTOR_DIR_GPIO_Port, MOTOR_DIR_Pin, GPIO_PIN_RESET);
        power_percent = -power_percent; // 計算用に絶対値（プラス）に戻す
    }

    // 2. パワーの限界値を100%に制限（安全装置）
    if (power_percent > 100.0f) {
        power_percent = 100.0f;
    }

    // 3. % を PWMのレジスタ値 (0 〜 999) に変換
    uint32_t pwm_value = (uint32_t)(11999.0f * (power_percent / 100.0f));

    // 4. タイマー（TIM2_CH1）の数値を更新してモーター出力！
    __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, pwm_value);
}

